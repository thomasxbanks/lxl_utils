/**
 * @constant {Function} - Adds the second given number to the first given number
 * @param {Number} a - The first given number
 * @param {Number} b - The second given number
 * @example add(1, 2) = 3
 * @returns {Number} - The result of the equation
 */
const add = (a, b) => a+b

module.exports = add
