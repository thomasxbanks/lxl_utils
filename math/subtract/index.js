/**
 * @constant {Function} - Subtracts the second given number from the first given number
 * @param {Number} a - The first given number
 * @param {Number} b - The second given number
 * @example subtract(1, 2) = -1
 * @returns {Number} - The result of the equation
 */
const subtract = (a, b) => a - b;

module.exports = subtract;
