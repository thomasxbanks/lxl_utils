/**
 * @constant {Function} - Calculates what percentage of the second given number the first given number is
 * @param {Number} a - The first given number
 * @param {Number} b - The second given number
 * @example percentage(1, 2) = 50
 * @returns {Number} - The result of the equation
 */
const percentage = (a, b) => a * 100 / b;

module.exports = percentage;
