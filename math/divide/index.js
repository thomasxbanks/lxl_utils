/**
 * @constant {Function} - Divides the first given number by the second given number
 * @param {Number} a - The first given number
 * @param {Number} b - The second given number
 * @example divide(1, 2) = 0.5
 * @returns {Number} - The result of the equation
 */
const divide = (a, b) => a/b

module.exports = divide