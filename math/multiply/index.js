/**
 * @constant {Function} - Multiplies the first given number by the second given number
 * @param {Number} a - The first given number
 * @param {Number} b - The second given number
 * @example multiply(1, 2) = 2
 * @returns {Number} - The result of the equation
 */
const multiply = (a, b) => a * b;

module.exports = multiply;
